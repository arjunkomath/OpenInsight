import {$} from 'bun';
import {createHash} from 'node:crypto';
import {existsSync} from 'node:fs';
import {mkdtemp, mkdir, readFile, rm, writeFile} from 'node:fs/promises';
import {tmpdir} from 'node:os';
import path from 'node:path';

const args = new Set(process.argv.slice(2));
const dryRun = args.has('--dry-run');
const distDir = path.resolve(process.env.DIST_DIR ?? 'dist');
const tag = process.env.GITHUB_REF_NAME;
const formulaPath = 'Formula/openinsight.rb';

const targets = ['darwin-arm64', 'linux-arm64', 'linux-x64'];

if (!tag) {
	throw new Error('GITHUB_REF_NAME is required, for example v0.9.0');
}

if (!tag.startsWith('v')) {
	throw new Error(
		`GITHUB_REF_NAME must be a version tag starting with "v": ${tag}`,
	);
}

const version = tag.slice(1);

async function sha256(filePath) {
	const data = await readFile(filePath);
	return createHash('sha256').update(data).digest('hex');
}

function releaseUrl(target) {
	return `https://github.com/arjunkomath/openinsight/releases/download/${tag}/openinsight-${target}.tar.gz`;
}

async function checksums() {
	const values = {};

	for (const target of targets) {
		const archive = path.join(distDir, `openinsight-${target}.tar.gz`);
		if (!existsSync(archive)) {
			throw new Error(`Missing release archive: ${archive}`);
		}

		values[target] = await sha256(archive);
	}

	return values;
}

function generateFormula(sums) {
	return `# typed: false
# frozen_string_literal: true

# This file was generated by openinsight. DO NOT EDIT.
class Openinsight < Formula
  desc "Query your database with natural language"
  homepage "https://github.com/arjunkomath/openinsight"
  version "${version}"
  license "MIT"

  on_macos do
    if Hardware::CPU.arm?
      url "${releaseUrl('darwin-arm64')}"
      sha256 "${sums['darwin-arm64']}"

      def install
        bin.install "openinsight"
      end
    end

  end

  on_linux do
    if Hardware::CPU.arm? && Hardware::CPU.is_64_bit?
      url "${releaseUrl('linux-arm64')}"
      sha256 "${sums['linux-arm64']}"

      def install
        bin.install "openinsight"
      end
    end

    if Hardware::CPU.intel? && Hardware::CPU.is_64_bit?
      url "${releaseUrl('linux-x64')}"
      sha256 "${sums['linux-x64']}"

      def install
        bin.install "openinsight"
      end
    end
  end

  test do
    assert_match "Usage", shell_output("#{bin}/openinsight --help")
  end
end
`;
}

function gitAuthEnv() {
	const token = process.env.HOMEBREW_TAP_TOKEN;
	if (!token) {
		throw new Error('HOMEBREW_TAP_TOKEN is required to update the tap');
	}

	const auth = Buffer.from(`x-access-token:${token}`).toString('base64');
	return {
		...process.env,
		GIT_CONFIG_COUNT: '1',
		GIT_CONFIG_KEY_0: 'http.https://github.com/.extraheader',
		GIT_CONFIG_VALUE_0: `AUTHORIZATION: basic ${auth}`,
	};
}

async function cloneTap(destination) {
	await $`git clone https://github.com/arjunkomath/homebrew-tap.git ${destination}`.env(
		gitAuthEnv(),
	);
}

async function publishFormula(formula) {
	const workdir = await mkdtemp(path.join(tmpdir(), 'openinsight-homebrew-'));
	const tapDir = path.join(workdir, 'homebrew-tap');

	try {
		await cloneTap(tapDir);

		const output = path.join(tapDir, formulaPath);
		await mkdir(path.dirname(output), {recursive: true});
		await writeFile(output, formula);

		await $`git config user.name github-actions[bot]`.cwd(tapDir);
		await $`git config user.email 41898282+github-actions[bot]@users.noreply.github.com`.cwd(
			tapDir,
		);
		await $`git add ${formulaPath}`.cwd(tapDir);

		const diff = await $`git diff --cached --quiet`.cwd(tapDir).nothrow();
		if (diff.exitCode === 0) {
			console.log('Formula is already up to date.');
			return;
		}

		await $`git commit -m ${`Update openinsight to ${version}`}`.cwd(tapDir);
		await $`git push`.cwd(tapDir).env(gitAuthEnv());
	} finally {
		await rm(workdir, {recursive: true, force: true});
	}
}

const sums = await checksums();
const formula = generateFormula(sums);

if (dryRun) {
	const outputPath = process.env.FORMULA_OUTPUT;
	if (outputPath) {
		await mkdir(path.dirname(path.resolve(outputPath)), {recursive: true});
		await writeFile(outputPath, formula);
		console.log(`Wrote formula to ${outputPath}`);
	} else {
		console.log(formula);
	}
} else {
	await publishFormula(formula);
}
